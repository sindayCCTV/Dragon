package Dragon.sinday.pojo;

public class fileFrom {
	/**
	 * 文件id
	 */
	private int fId;
	/**
	 * 文件路径
	 */
	private String fPath;
	/**
	 * 文件类型1为视频 2位图片
	 */
	private int fType;
	
	
	
	public int getfId() {
		return fId;
	}
	public void setfId(int fId) {
		this.fId = fId;
	}
 
	public String getfPath() {
		return fPath;
	}
	public void setfPath(String fPath) {
		this.fPath = fPath;
	}
	public int getfType() {
		return fType;
	}
	public void setfType(int fType) {
		this.fType = fType;
	}
	
	
	
	
}
